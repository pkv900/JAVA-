
public class FloatLiteralEx {
	public static void main(String[] args)
	{
		//Decimal-form literal
		float a = 101.230f;
		
		//It also acts as decimal literal
		float b = 0123.222f;
		
		//Hexa Decimal form
		float c = 0x123.222f;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
	}

}
